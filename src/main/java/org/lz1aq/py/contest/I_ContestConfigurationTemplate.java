package org.lz1aq.py.contest;

/**
 * Holds the contest configuration parameters with their possible values that
 * can be selected.
 * 
 * For each parameter the possible configuration values are supplied (see getParamValues())
 */
public interface I_ContestConfigurationTemplate
{
  String getContestName();
  
  int getParamCount();
  String getParamName(int index);
  String[] getParamValues(int index);
}
